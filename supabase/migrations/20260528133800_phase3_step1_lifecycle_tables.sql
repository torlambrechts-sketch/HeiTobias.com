-- ============ guidance_items ============
-- Manager-facing guidance. EVERY row MUST cite at least one framework_id —
-- enforced by the CHECK on framework_ids array length. inputs_json carries
-- the structured data that fed the generation (audit trail). action +
-- action_at let the manager record what they did with the guidance; the
-- suggestion INFORMS, never decides.
create table public.guidance_items (
  id              uuid primary key default extensions.gen_random_uuid(),
  org_id          uuid not null references public.organizations(id),
  person_id       uuid not null references public.people(id),
  consent_id      uuid not null references public.consent_grants(id),
  kind            public.guidance_kind not null,
  framework_ids   uuid[] not null,
  inputs_json     jsonb not null default '{}'::jsonb,
  output_json     jsonb not null,
  generated_at    timestamptz not null default now(),
  generated_by    uuid references public.people(id),
  action          public.guidance_action,
  action_at       timestamptz,
  action_notes    text,
  validity_status public.validity_status not null default 'dev_stub',
  _dev_stub       boolean not null default true,
  created_at      timestamptz not null default now(),
  updated_at      timestamptz not null default now(),
  constraint chk_guidance_grounded check (array_length(framework_ids, 1) >= 1),
  constraint chk_guidance_validated_real
    check (validity_status <> 'validated'
           or (output_json <> '{}'::jsonb and coalesce(_dev_stub, false) = false))
);
create index guidance_items_person_idx on public.guidance_items (person_id, generated_at desc);

create trigger trg_touch_guidance_items before update on public.guidance_items for each row execute function public.set_updated_at();
create trigger trg_audit_guidance_items after insert or update or delete on public.guidance_items for each row execute function public._audit_row();

