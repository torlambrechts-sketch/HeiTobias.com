-- phase3_step1_lifecycle_tables — the 7 module tables Phase 3 needs.
--
-- DEFAULT-DENY RLS on every table. EVERY visibility predicate is gated by
-- consent_active(_, 'ongoing_management') OR is_self() — never a membership
-- alone (per PHASE0 §4.4) and never a different consent purpose.
--
-- Two principles structurally enforced here:
--   * Developmental, never surveillance — every personal-data table carries
--     a self-read leg (the employee can see what the manager sees) AND the
--     four-quadrant signal labels are framed developmentally (growth_gap,
--     flight_risk, etc. — never "underperforming"). Hidden manager-only
--     scoring is impossible because the RLS self-leg cannot be turned off.
--   * Grounded, never fabricated — guidance_items.framework_ids is a
--     uuid[] with a CHECK requiring at least one citation. The DEV-STUB
--     seam matches Phase 1 + Phase 2: validity_status + _dev_stub +
--     refusal of 'validated' rows that carry stub values.

-- Enums for the new domain language.
create type public.refit_quadrant as enum (
  'stable_fit', 'growth_gap', 'flight_risk', 'emerging_misfit'
);
comment on type public.refit_quadrant is
  'Four-quadrant re-fit signal — developmental framing, never deficit-based.';

create type public.guidance_kind as enum (
  'one_on_one_prep', 'growth_focus', 'check_in_design', 'team_gap_callout'
);

create type public.guidance_action as enum (
  'acted_on', 'noted', 'snoozed', 'dismissed'
);

create type public.outcome_kind as enum (
  'promoted', 'lateral_move', 'role_change', 'left_org', 'retained_quarterly'
);

-- ============ pulse_checkins ============
-- Employee-submitted periodic check-in. EMPLOYEE OWNS THE DATA: every row
-- is is_self()-readable. Manager visibility requires has_permission +
-- in_scope + active ongoing_management consent. Never collected in the
-- background — the row only exists if the employee submitted it.
create table public.pulse_checkins (
  id           uuid primary key default extensions.gen_random_uuid(),
  org_id       uuid not null references public.organizations(id),
  person_id    uuid not null references public.people(id),
  consent_id   uuid not null references public.consent_grants(id),
  template_id  uuid references public.frameworks(id),
  submitted_at timestamptz not null default now(),
  body_json    jsonb not null default '{}'::jsonb,
  created_at   timestamptz not null default now(),
  updated_at   timestamptz not null default now()
);
create index pulse_checkins_person_idx on public.pulse_checkins (person_id, submitted_at desc);

create trigger trg_touch_pulse_checkins  before update on public.pulse_checkins for each row execute function public.set_updated_at();
create trigger trg_audit_pulse_checkins  after insert or update or delete on public.pulse_checkins for each row execute function public._audit_row();

alter table public.pulse_checkins enable row level security;
alter table public.pulse_checkins force  row level security;

create policy pulse_checkins_select on public.pulse_checkins for select using (
  public.is_self(person_id)
  or (public.has_permission(org_id, 'pulse.read')
      and public.in_scope(org_id, person_id)
      and public.consent_active(consent_id, 'ongoing_management'))
);
create policy pulse_checkins_insert on public.pulse_checkins for insert with check (
  public.is_self(person_id) and public.consent_active(consent_id, 'ongoing_management')
);
-- UPDATE only by the data subject (developmental discipline — managers can't
-- silently mutate an employee's self-report).
create policy pulse_checkins_update on public.pulse_checkins for update
  using (public.is_self(person_id))
  with check (public.is_self(person_id));

-- ============ signals ============
-- Derived signals from pulses + structured profile data. Generated by RPC,
-- not collected in the background. The source_json field MUST cite the
-- structured data it was computed from (pulse_id, framework_id, etc.) so
-- the employee can see, on their self-view, exactly what fed each signal.
create table public.signals (
  id           uuid primary key default extensions.gen_random_uuid(),
  org_id       uuid not null references public.organizations(id),
  person_id    uuid not null references public.people(id),
  consent_id   uuid not null references public.consent_grants(id),
  kind         text not null,
  value_json   jsonb not null default '{}'::jsonb,
  source_json  jsonb not null default '{}'::jsonb,
  framework_id uuid references public.frameworks(id),
  generated_at timestamptz not null default now(),
  generated_by uuid references public.people(id),
  validity_status public.validity_status not null default 'dev_stub',
  _dev_stub    boolean not null default true,
  created_at   timestamptz not null default now(),
  updated_at   timestamptz not null default now(),
  constraint chk_signals_validated_real
    check (validity_status <> 'validated'
           or (value_json <> '{}'::jsonb and coalesce(_dev_stub, false) = false)),
  -- Developmental discipline: signal must cite its source structurally.
  constraint chk_signals_grounded
    check (source_json <> '{}'::jsonb)
);
create index signals_person_idx on public.signals (person_id, generated_at desc);

create trigger trg_touch_signals before update on public.signals for each row execute function public.set_updated_at();
create trigger trg_audit_signals after insert or update or delete on public.signals for each row execute function public._audit_row();

alter table public.signals enable row level security;
alter table public.signals force  row level security;

create policy signals_select on public.signals for select using (
  public.is_self(person_id)
  or (public.has_permission(org_id, 'pulse.read')
      and public.in_scope(org_id, person_id)
      and public.consent_active(consent_id, 'ongoing_management'))
);
create policy signals_insert on public.signals for insert with check (
  public.has_permission(org_id, 'pulse.read')
  and public.consent_active(consent_id, 'ongoing_management')
);

