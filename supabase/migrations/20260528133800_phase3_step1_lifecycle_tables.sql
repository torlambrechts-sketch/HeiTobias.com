-- ============ Permissions ============
-- Add the Phase 3 permission keys. Grant the appropriate role × permission
-- pairs so the seeded users can actually use the new surfaces.
insert into public.rbac_permissions (key, description) values
  ('pulse.read',         'Read pulse_checkins and signals for in-scope people (consent-gated)'),
  ('refit.read',         'Read refit_evaluations for in-scope people (consent-gated)'),
  ('refit.compute',      'Compute a re-fit evaluation (writes refit_evaluations)'),
  ('guidance.read',      'Read guidance_items + growth_conversations (consent-gated)'),
  ('guidance.generate',  'Generate guidance_items via the composer'),
  ('team.read',          'Read team_composition_snapshots')
on conflict (key) do nothing;

-- Grant the new permissions to org_admin (everything) + people_ops_admin
-- (operational) + manager (their reports). Recruiter does NOT get any
-- of these — recruiter scope ends at placement.
insert into public.rbac_role_permissions (role_id, permission_id)
select r.id, p.id
  from public.rbac_roles r
  cross join public.rbac_permissions p
  where r.org_id is null
    and r.key in ('org_admin','people_ops_admin')
    and p.key in ('pulse.read','refit.read','refit.compute','guidance.read','guidance.generate','team.read')
on conflict do nothing;

insert into public.rbac_role_permissions (role_id, permission_id)
select r.id, p.id
  from public.rbac_roles r
  cross join public.rbac_permissions p
  where r.org_id is null
    and r.key = 'manager'
    and p.key in ('pulse.read','refit.read','refit.compute','guidance.read','guidance.generate','team.read')
on conflict do nothing;

